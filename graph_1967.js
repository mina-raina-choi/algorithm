function solution(params) {
  const input = params.split("\n")
  const n = parseInt(input.shift())
  const map = new Map()
  for (let i = 0; i < n - 1; i++) {
    const [start, end, cost] = input
      .shift()
      .split(" ")
      .map(a => +a)
    if (map.get(start)) {
      map.get(start).push([end, cost])
    } else {
      map.set(start, [[end, cost]])
    }
  }
  //   console.log(map)

  let diameter = 0
  for (let i = 1; i <= n; i++) {
    const array = bfs(i)
    const temp = array[array.length - 1][1] + array[0][1]
    diameter = Math.max(diameter, temp)
  }

  console.log(diameter)

  function bfs(start) {
    const q = [[start, 0]]
    let array = []
    while (q.length > 0) {
      let qLen = q.length
      array = [...q]
      while (qLen) {
        const [node, sofar] = q.shift()
        // console.log("node, sofar", node, sofar)
        const newNodes = map.get(node)
        for (let i in newNodes) {
          const [nNode, cost] = newNodes[i]
          q.push([nNode, cost + sofar])
        }
        qLen--
      }
      console.log("depth", array)
    }
    return array
  }
}

